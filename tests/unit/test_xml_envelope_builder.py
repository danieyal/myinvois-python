# ---------------------------------------------------------------------------
# 8. Determinism + canonicalisation
# ---------------------------------------------------------------------------


class TestXmlDeterminism:
    @_maybe_xfail
    def test_two_runs_produce_identical_string(self) -> None:
        a = _builder().build_xml()
        b = _builder().build_xml()
        assert a == b

    @_maybe_xfail
    def test_canonicalisation_idempotent(self) -> None:
        # Re-canonicalising the output should produce the same bytes — this
        # proves the output is already in c14n-1.0 inclusive form (Phase 4
        # will rely on this invariant for the signature digest computation).
        # NB: the c14n mode MUST match the builder's (exclusive=False) — exclusive
        # c14n pulls the xmlns declarations down to the first-usage element rather
        # than keeping them on root, which is a strictly different canonical form
        # and produces naturally-different bytes (not an idempotency failure).
        out = _builder().build_xml()
        from lxml import etree

        reparsed = etree.fromstring(out.encode("utf-8"))
        re_c14n = etree.tostring(
            reparsed,
            method="c14n",
            exclusive=False,
            with_comments=False,
        ).decode("utf-8")
        assert re_c14n == out


# ---------------------------------------------------------------------------
# 9. The big one — byte-for-byte parity with the golden fixture
# ---------------------------------------------------------------------------


class TestReferenceByteParity:
    @_maybe_xfail
    def test_byte_for_byte_matches_reference_xml_output(self) -> None:
        """The single test that anchors Phase 3c-XML correctness.

        ``tests/fixtures/golden_invoice_unsigned.xml`` is the exact output of
        the reference ``XmlDocumentBuilder::build()``
        on the invoice mirrored into
        ``_sample_invoice()``. The fixture was md5-sum-diffed against the reference
        output at fixture-population time and committed.
        """
        actual = _builder().build_xml()
        golden = _golden_xml()
        if actual != golden:
            # Diagnostics: walk to the first divergent character.
            n = min(len(actual), len(golden))
            for i in range(n):
                if actual[i] != golden[i]:
                    raise AssertionError(
                        "envelope XML drifted from the reference.\n"
                        f"  diff at index {i}:\n"
                        f"  actual[{i}]: ...{actual[max(0, i - 60) : i + 60]!r}\n"
                        f"  golden[{i}]: ...{golden[max(0, i - 60) : i + 60]!r}"
                    )
            longer = "actual" if len(actual) > len(golden) else "golden"
            raise AssertionError(
                f"strings diverge in length; {longer} longer "
                f"(actual={len(actual)}, golden={len(golden)})"
            )
