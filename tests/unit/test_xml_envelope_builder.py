"""RED tests for ``XmlEnvelopeBuilder``.

These tests are the outer verification ring of Phase 3c (XML half). The
canonical reference output was generated by running
the reference implementation's ``XmlDocumentBuilder::build()`` on the same
invoice materialised by ``_sample_invoice()`` (mirrored field-for-field).
The resulting bytes are
pinned at ``tests/fixtures/golden_invoice_unsigned.xml`` (md5-checked
against the reference output during fixture population).

Until ``XmlEnvelopeBuilder.build_xml()`` is green (Phase 3c-XML Step 3),
every test in this file should be marked ``xfail`` to keep the rest of
the suite green, then unmarked once the implementation lands. Switching
``EXPECT_IMPLEMENTED`` to ``True`` flips all of them to required.

Note: the reference XmlDocumentBuilder finishes with C14N (canonical
XML 1.1) and strips the XML declaration. lxml matches this when
``etree.tostring(method='c14n', exclusive=True, with_comments=False)``
is called on a freshly-parsed tree — same code path Phase 4's signature
digest will use.
"""

from __future__ import annotations

import pytest

# Flip to True when the XmlEnvelopeBuilder implementation lands.
EXPECT_IMPLEMENTED = True

_XFAIL_REASON = (
    "XmlEnvelopeBuilder not yet implemented (Phase 3c-XML Step 3). "
    "Set EXPECT_IMPLEMENTED=True at the top of this file once green."
)


def _maybe_xfail(func):
    """Mark every RED test as xfail until the implementation lands."""
    if EXPECT_IMPLEMENTED:
        return func
    return pytest.mark.xfail(reason=_XFAIL_REASON, strict=True)(func)


# ---------------------------------------------------------------------------
# Fixture reuse — sample invoice is the same shape used by the JSON builder.
# ---------------------------------------------------------------------------

import sys  # noqa: E402

sys.path.insert(0, "tests/unit")
import test_envelope_builder as _json_fixture  # noqa: E402

_sample_invoice = _json_fixture._sample_invoice


# ---------------------------------------------------------------------------
# Lazy imports so importing this module doesn't blow up before the impl exists.
# ---------------------------------------------------------------------------


def _builder():
    from myinvois.ubl.builders import XmlEnvelopeBuilder

    return XmlEnvelopeBuilder(_sample_invoice())


def _golden_xml() -> str:
    from pathlib import Path

    p = Path(__file__).resolve().parent.parent / "fixtures" / "golden_invoice_unsigned.xml"
    return p.read_text(encoding="utf-8")


# ---------------------------------------------------------------------------
# 1. Outer envelope + namespaces
# ---------------------------------------------------------------------------


class TestXmlEnvelopeShape:
    @_maybe_xfail
    def test_build_xml_returns_str(self) -> None:
        assert isinstance(_builder().build_xml(), str)

    @_maybe_xfail
    def test_root_element_is_invoice_with_default_namespace(self) -> None:
        out = _builder().build_xml()
        # Canonical with-namespace ordering: xmlns (default) first, then
        # xmlns:cac, xmlns:cbc, xmlns:ext alphabetical by prefix.
        assert out.startswith(
            '<Invoice xmlns="urn:oasis:names:specification:ubl:schema:xsd:Invoice-2" '
            'xmlns:cac="urn:oasis:names:specification:ubl:schema:xsd:CommonAggregateComponents-2" '
            'xmlns:cbc="urn:oasis:names:specification:ubl:schema:xsd:CommonBasicComponents-2" '
            'xmlns:ext="urn:oasis:names:specification:ubl:schema:xsd:CommonExtensionComponents-2">'
        )

    @_maybe_xfail
    def test_output_has_no_xml_declaration(self) -> None:
        s = _builder().build_xml()
        assert not s.startswith("<?xml")
        assert "<?xml" not in s

    @_maybe_xfail
    def test_output_has_no_indentation_or_whitespace_between_elements(self) -> None:
        # C14N-1.1 strips inter-element whitespace. Only text content retains
        # spaces (e.g. "Lot 66, Bangunan Merdeka").
        s = _builder().build_xml()
        # No newlines, tabs, or carriage returns anywhere.
        assert "\n" not in s and "\t" not in s and "\r" not in s
        # No `> <` between adjacent elements (would indicate whitespace text).
        assert "> <" not in s

    @_maybe_xfail
    def test_top_level_invoice_closes_at_end(self) -> None:
        s = _builder().build_xml()
        assert s.endswith("</Invoice>")


